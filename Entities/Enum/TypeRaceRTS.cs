using UnityEngine;

namespace FireNBM
{
    /// <summary>
    /// Enum đại diện cho các chủng tộc trong game RTS.
    /// Điều này giúp phân loại các chủng tộc có sẵn trong game.
    /// </summary>
    public enum TypeRaceRTS
    {
        /// <summary>
        /// Chủng tộc Terran, một chủng tộc nhân loại với các công nghệ tiên tiến và khả năng cơ động.
        /// </summary>
        Terran,

        /// <summary>
        /// Chủng tộc Zerg, một chủng tộc sinh vật ngoại lai với khả năng tấn công bầy đàn và thích nghi nhanh chóng.
        /// </summary>
        Zerg,

        /// <summary>
        /// Chủng tộc Protoss, một chủng tộc tiên tiến với công nghệ mạnh mẽ và các khả năng tâm linh.
        /// </summary>
        Protoss
    }
}
